print("Declaração simples:")
print("Inteiro:", 5 + 15)
print("Flutuante:", 5.7 + 17.28)
print("Booleano:", True, False)
print("String:", "Python!\n") # "\n" é usado para pular linha.

print("Declaração explícita:")
print("Inteiro:", int(5 + 15))
print("Flutuante:", float(5.7 + 17.28))
print("Booleano:", bool(True), bool(False))
print("String:", str("Python!\n"))

# A forma explícita é útil para indicar para o compilador o quanto de memória será necessário alocar para armazenar aquele dado.
# Também é útil para poder acessar os métodos que o Python possui para tipo de dado.